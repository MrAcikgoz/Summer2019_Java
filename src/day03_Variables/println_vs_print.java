package day03_Variables;

public class println_vs_print {

	public static void main (String[] args) {
		
		// the only characters we can give for any names is : _ and $ 
		
		System.out.println("Hello Cybertek");
		System.out.println("School");
		
		// Println : the printing always print from a new line
		
		System.out.println("============================");
		System.out.print("Hello Cybertek");
		System.out.print("School");
		// Print: the printing takes place from where it's left off
		
		
	}
}
